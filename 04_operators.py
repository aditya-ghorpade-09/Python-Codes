# arethmatic opertaors

print("Arethmatic Operator")
a = 1
b = 2
c = a + b
print(c)

# assignment operator

print("Assignment Operator")
x = 1
print(x)

y = 6
y += 3
print(y)

z = 6
z -= 3
print(z)

p = 6
p *= 3
print(p)

q = 6 
q /= 3
print(q)

# comparison or relational operator

print("Comparison operator")
n = 1 == 1
print(n)
m = 1 > 0
print(m)
l = 3 < 1
print(l)
# also >= <=

# logical operator

print("Logical Operator")
f = True and True
print(f)
t = False or False
print(t)
r = not(False)
print(r)